    private sealed class RunningOperation : IDisposable
    {
        private readonly SessionView _owner;
        private bool _disposed;

        public RunningOperation(SessionView owner)
        {
            _owner = owner;
        }

        public void Dispose()
        {
            if (_disposed)
            {
                return;
            }

            _disposed = true;
            _owner.EndRunningOperation();
        }
    }

